    private void validarSesion(){
        
        
        user  =  this.jTextField1.getText();
        password = ""+ new String (this.jPasswordField1.getPassword());
        
        if (user.equals("") || password.equals("")) {
          JOptionPane.showMessageDialog(null, "Por Favor llene los dos campos");   
          
          
        }
        else{
             querys.executeQuery("SELECT * FROM usuarios WHERE nombre = '"+user+"' AND password='"+password+"';");
            
             ResultSet resultados = querys.resultados;
             
             try{
                 resultados.first();
                 System.out.println(resultados.getInt("id"));
                 JOptionPane.showMessageDialog(null, "Logeo con Exito");   
                 
             }
             catch(SQLException e){
                 
                JOptionPane.showMessageDialog(null, "Usuario o contrase침a incorrectos");   
                this.setVisible(false);
                
             }
        }
            
        }
    
